
#ifndef JSONCPP_DEFINES_H
#define JSONCPP_DEFINES_H


enum class TokenType
{
    CURLY_OPEN,
    CURLY_CLOSE,

    SQUARE_OPEN,
    SQUARE_CLOSE,

    COLON,
    COMMA,

    KEY,

    DATA_STR,
    DATA_BOOL,
    DATA_INT,
    DATA_DOUBLE,
};


enum class State {
    OBJECT_PARSING,
    ARRAY_PARSING
};


enum class ParseError
{
    NOT_ERROR,

    FIRST_CHAR_NOT_CURLY_OPEN,
    LAST_CHAR_NOT_CURLY_CLOSE,

    BRACKET_CURLY_ERROR,
    BRACKET_SQUARE_ERROR,

    IMPROPER_TOKEN_AFTER_COMMA,
    IMPROPER_TOKEN_AFTER_COLON,
    IMPROPER_TOKEN_AFTER_CURLY_CLOSE,
    IMPROPER_TOKEN_AFTER_CURLY_OPEN,
    IMPROPER_TOKEN_AFTER_DATA_BOOL,
    IMPROPER_TOKEN_AFTER_DATA_DOUBLE,
    IMPROPER_TOKEN_AFTER_DATA_INT,
    IMPROPER_TOKEN_AFTER_STRING,

    STRING_NOT_ENDED,

    UNKNOWN_SYMBOL,
};

#endif

