
#ifndef JSONCPP_TOKEN_TYPE_H
#define JSONCPP_TOKEN_TYPE_H


enum class TokenType
{
    CURLY_OPEN,
    CURLY_CLOSE,

    SQUARE_OPEN,
    SQUARE_CLOSE,

    COLON,
    COMMA,

    DATA_STR,
    DATA_BOOL,
    DATA_INT,
    DATA_DOUBLE,
};


enum class ParseError
{
    NOT_ERROR,

    FIRST_CHAR_NOT_CURLY_OPEN,
    LAST_CHAR_NOT_CURLY_CLOSE,

    BRACKET_CURLY_ERROR,
    BRACKET_SQUARE_ERROR,

    IMPROPER_TOKEN_AFTER_COLON,
    IMPROPER_TOKEN_AFTER_CURLY_OPEN,
    IMPROPER_TOKEN_AFTER_STRING,

    STRING_NOT_ENDED,

    UNKNOWN_SYMBOL,
};

#endif

